/*

    1. Problem Briefing: Given a sequence A w/ length N and integer P. Find the longest
    contiguous sequence such that sum of all elements in A is greater than or equal to P.

    2. Keyword: prefix-sum, sorting

    3. Solution:
        * Key Idea: Find the minimum index j such that sum from j to i is not less than P

        * Implementation:
            - Sort the prefix sum array while maintaining index
            - Iterate i from 1 to n and maintain a pointer j at sorted prefix sum array.
            If s[j] - s[i] >= p, increase j and update minimum index (because we want index
            as small as possible). If j>=0, update the answer.

    4. Code: Written 10:37PM, Dec 6th, 2018

*/
