//Process
void print(int c)
{
    if(!c) return;
    print((c - 1) / 26);
    putchar(((c - 1) % 26) + 'A');
}
void Solve()
{
    char str[202020], t[202020], *p;
    scanf("%s", &str);

    if(sscanf(str, "R%dC%d", &r, &c) == 2)
    {
        print(c);
        printf("%d\n", r);
    }
    else
    {
        sscanf(str, "%[A-Z]%d", t, &r);
        for(p = t, c = 0; *p; p++)
        {
            c = c * 26 + int(*p - 'A' + 1);
        }
        printf("R%dC%d\n", r, c);
    }
}


