//Check
void Solve()
{
    if(m * 9 < s || (s == 0 && m > 1))
    {
        cout << -1 << sp << -1 << el;
        return;
    }

    int s1 = s;
    while(s1 > 9)
    {
        s1 -= 9;
        x += '9';
        y = '9' + y;
        --m;
    }

    x += char(s1+'0');
    x += string(m-1, '0');

    if(m >= 2)
    {
        y = char(s1+'0'-1) + y;
        y = string(m-2, '0') + y;
        y = '1' + y;
    }
    else y = char(s1+'0') + y;

    cout << y << sp << x;
}


